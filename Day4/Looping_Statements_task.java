package d4;
import java.util.Scanner;
public class Looping_Statements {

	public static void main(String[] args) {
		//looping statements
		//1. while loop   2.do while loop 
		//3. for loop     4.nested loop   5. for each loop 
		
		//printing first 10 numbers in reverse and print their sum
		int sum= 0;
		for(int i=10;i>0;i--)
		{
			System.out.println(i);
			sum++;
		}
		System.out.println("Sum of numbers = " + sum);
		
		}
		
	}
	
